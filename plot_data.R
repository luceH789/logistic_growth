#Script to plot the logistic growth data

growth_data <- read.csv("experiment.csv")

library(ggplot2)

ggplot(aes(t,N), data = growth_data) +
  
  geom_point() +
  
  xlab("t") +
  
  ylab("y") +
  
  theme_bw()

ggplot(aes(t,N), data = growth_data) +
  
  geom_point() +
  
  xlab("t") +
  
  ylab("y") +
  
  scale_y_continuous(trans='log10') #this transforms the y axis into a log scale
#good practice as doesn't change the raw data
